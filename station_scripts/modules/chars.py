# Most of the drawing are 3x2 (17p x 17p)
def draw(lcd_device, draw_arr):
    for i in range(6):
        lcd_device.create_char(i, draw_arr[i])
    lcd_device.cursor_pos = (0, 13)  # Last 3 spaces of row 1
    lcd_device.write_string('\x00\x01\x02')  # Custom characters 0, 1, 2
    lcd_device.cursor_pos = (1, 13)  # Last 3 spaces of row 2
    lcd_device.write_string('\x03\x04\x05')  # Custom characters 3, 4, 5
